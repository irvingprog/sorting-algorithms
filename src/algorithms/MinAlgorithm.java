package algorithms;

/**
 *
 * @author orh
 */
public class MinAlgorithm {

    public static int exec(int[] data) {
        int min = data[0];
        for (int i = 1; i < data.length; i++) {
            if (data[i] < min) {
                min = data[i];
            }
        }
        
        return min;
    }
    
}
